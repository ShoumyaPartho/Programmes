void solve() {
	int n, v, fuel, cost = 0, pos = 1; cin >> n >> v;

	fuel = min(v, n - pos);
	cost += (fuel * pos);

	while (pos < n) {
		pos++; fuel--;

		if (fuel < (n - pos)) {
			fuel++;
			cost += pos;
		}
	}

	cout << cost << endl;
}

