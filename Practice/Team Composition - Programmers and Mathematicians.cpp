void solve() {
	ll tc, a, b; cin >> tc;

	while (tc--) {
		cin >> a >> b;

		if (a > b)
			swap(a, b);

		if (a == 0 or b == 0)
			cout << 0 << endl;

		else if (b >= (a * 3ll))
			cout << a << endl;

		else
			cout << (a + b) / 4ll << endl;
	}
}

